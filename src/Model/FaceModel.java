package Model;

public class FaceModel {
	
	private String faceno;
	private String emworkid;
	private String emname;
	private String facedate;
	private String facepath;
	
	public String getFaceno() {
		return faceno;
	}
	public void setFaceno(String faceno) {
		this.faceno = faceno;
	}
	
	public String getEmworkid() {
		return emworkid;
	}
	public void setEmworkid(String emworkid) {
		this.emworkid = emworkid;
	}
	public String getEmname() {
		return emname;
	}
	public void setEmname(String emname) {
		this.emname = emname;
	}
	public String getFacedate() {
		return facedate;
	}
	public void setFacedate(String facedate) {
		this.facedate = facedate;
	}
	public String getFacepath() {
		return facepath;
	}
	public void setFacepath(String facepath) {
		this.facepath = facepath;
	}

}
